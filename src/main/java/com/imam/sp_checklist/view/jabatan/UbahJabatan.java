    public Jabatan ubahJabatan(Jabatan param){
        
        jabatan = param;
        
        if(cekBoxPengawas.isSelected()){
            cekBoxPengawas.setText("Ya");
        }else{
            cekBoxPengawas.setText("Tidak");
        }

        
        txtKode.setText(jabatan.getId());
        txtNama.setText(jabatan.getNama());
        
        lblBuatBy.setText(jabatan.getBuatBy());
        lblWaktuBuat.setText(String.valueOf(jabatan.getWaktuDibuat()));
        cekBoxPengawas.setSelected(jabatan.isPengawas());
        
        setLocationRelativeTo(this);
        setVisible(true);
        
        return jabatan;
        
    }
    
    
    

