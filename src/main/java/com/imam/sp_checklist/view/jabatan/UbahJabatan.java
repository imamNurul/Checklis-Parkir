    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        Pengguna pgn = LoginManager.getInstance().getPengguna();
        
            jabatan = new Jabatan();
            jabatan.setBuatBy(lblBuatBy.getText());
            jabatan.setWaktuDibuat(Timestamp.valueOf(lblWaktuBuat.getText()));
            jabatan.setWaktuUbah(new java.sql.Timestamp(new java.util.Date().getTime()));
            jabatan.setUbahBy(pgn.getKaryawan().getNama());
            jabatan.setId(txtKode.getText());
            jabatan.setNama(txtNama.getText());
            jabatan.setPengawas(cekBoxPengawas.isSelected());

            JabatanValidator validator = SpringManager.getInstance().getBean(JabatanValidator.class);
            JabatanService service = SpringManager.getInstance().getBean(JabatanService.class);


            try {
                validator.validate(jabatan);
                service.update(jabatan);
                dispose();
            } catch (ValidatorException ex) {
                messageComponent1.showWarning(ex.getMessage());
            }catch (DataAccessException ex) {
                messageComponent1.showWarning(ex.getRootCause().getMessage());
            }
        
        

    }//GEN-LAST:event_btnSimpanActionPerformed

