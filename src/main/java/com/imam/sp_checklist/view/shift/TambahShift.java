    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        Locale locale = Locale.forLanguageTag("in-ID");
        Pengguna pgn = LoginManager.getInstance().getPengguna();
        
        ShiftService ks = SpringManager.getInstance().getBean(ShiftService.class);
        Shift kd = ks.find(txtKode.getText());
        
        System.out.println("jam selesai: "+jamSelesai.getValue());
        SimpleDateFormat tm = new SimpleDateFormat("HH:mm:ss",locale);
        
        Date dts = (Date) jamSelesai.getValue();
        Date dtm = (Date) jamMulai.getValue();
        String jmStr = tm.format(dtm);
        String jmEnd = tm.format(dts);
        System.out.println("jam selesai format: "+jmEnd);
        System.out.println("jam mulai format: "+jmStr);
        
        if(kd != null){
            messageComponent1.showWarning("Kode Shift sudah ada");
        }else{
            
            shift = new Shift();
            shift.setBuatBy(pgn.getKaryawan().getNama());
            shift.setWaktuDibuat(new java.sql.Timestamp(new java.util.Date().getTime()));
            shift.setWaktuUbah(new java.sql.Timestamp(new java.util.Date().getTime()));
            shift.setUbahBy(pgn.getKaryawan().getNama());
            shift.setId(txtKode.getText());
            shift.setNama(txtNama.getText());
            shift.setJamMulai(new java.sql.Time(jamMulai.getValue().hashCode()));
            shift.setJamSelesai(new java.sql.Time(jamSelesai.getValue().hashCode()));

            ShiftValidator validator = SpringManager.getInstance().getBean(ShiftValidator.class);
            ShiftService service = SpringManager.getInstance().getBean(ShiftService.class);


            try {
                validator.validate(shift);
                service.save(shift);
                dispose();
            } catch (ValidatorException ex) {
                messageComponent1.showWarning(ex.getMessage());
            }catch (DataAccessException ex) {
                messageComponent1.showWarning(ex.getRootCause().getMessage());
            }

            
        }
        
    }//GEN-LAST:event_btnSimpanActionPerformed


